public class varDemo {
    public static void main(String [] args) {
        // int var 
        var intNumbre = 10;
        System.out.println("int var demo number is :"+intNumbre);

        // float var 
        var floatNumbre = 10.0;
        System.out.println("float var demo number is :"+floatNumbre);

        // char
        var charChar = 'A';
        System.out.println("char var demo char is :"+charChar);

        //String
        var stringString = "Hello A";
        System.out.println("string var demo string is :"+stringString);

    }
}